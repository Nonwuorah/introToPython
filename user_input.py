# user_input.py is a Python script that asks the user for their name, age, and location, and then prints a personalized message to the user.


# Asking for user input
name = input("What is your name? ")
age = input("How old are you? ")
location = input("Where do you live? ")
favorite_color = input("What is your favorite color? ")
hobby = input("What is your hobby? ")

# Printing the personalized message
print(f"Hello {name}, you are {age} years old and live in {location}.")
print(f"Your favorite color is {favorite_color} and you enjoy {hobby}.")
